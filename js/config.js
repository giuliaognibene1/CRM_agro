function getBase64Image(img) {

    let canvas = document.createElement("canvas");

    canvas.width = img.width;
    canvas.height = img.height;

    let ctx = canvas.getContext("2d");
    ctx.drawImage(img, 0, 0);

    return canvas.toDataURL("image/png");
}


function disegnaHeader(doc, logo, cliente, data, pagina) {

    doc.addImage(logo, "PNG", 20, 10, 22, 22);

    doc.setFont("helvetica", "bold");
    doc.setFontSize(12);
    doc.text("AGRO+ SRL", 50, 15);

    doc.setFont("helvetica", "normal");
    doc.setFontSize(9);
    doc.text("Via Sacco e Vanzetti 21", 50, 21);
    doc.text("42021 Bibbiano (RE)", 50, 26);
    doc.text("info@agropiu.com - 0522 875924", 50, 31);

    doc.setFontSize(10);
    doc.text("Cliente: " + cliente, 20, 45);
    doc.text("Data: " + data, 120, 45);
    doc.text("Pagina " + pagina, 180, 45);
}


function generaPDF() {

    const { jsPDF } = window.jspdf;
    const doc = new jsPDF();

    let cliente = document.getElementById("cliente").value.trim();

    if (!cliente) {
        alert("Inserisci il nome del cliente prima di esportare il PDF");
        return;
    }

    let data = new Date().toLocaleDateString();

    let img = new Image();
    img.src = "logo.png";

    img.onload = function () {

        let logo = getBase64Image(img);

        creaPDF(doc, logo, cliente, data);
    };
}


function creaPDF(doc, logo, cliente, data) {

    let righe = document.querySelectorAll("#totale li");

    let pageHeight = doc.internal.pageSize.height;

    let yStart = 65;
    let y = yStart;

    let pagina = 1;

    disegnaHeader(doc, logo, cliente, data, pagina);

    righe.forEach(riga => {

        let qty = riga.querySelector(".qty").innerText;
        let sep = riga.querySelector(".sep").innerText;
        let nome = riga.querySelector(".name").innerText;

        if (y > pageHeight - 20) {

            doc.addPage();

            pagina++;
            y = yStart;

            disegnaHeader(doc, logo, cliente, data, pagina);
        }

        doc.text(qty, 20, y);
        doc.text(sep, 35, y);
        doc.text(nome, 45, y);

        y += 8;
    });

    doc.save("configurazione-pivot.pdf");
}