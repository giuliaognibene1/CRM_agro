/* =====================================================
   CALCOLO CONFIGURAZIONE
   ===================================================== */

function aggiorna() {

    let totale = {};
    let perCategoria = {};

    function aggiungi(componenti, categoria) {

        if (!perCategoria[categoria]) {
            perCategoria[categoria] = {};
        }

        for (let pezzo in componenti) {

            if (!totale[pezzo]) totale[pezzo] = 0;
            totale[pezzo] += componenti[pezzo];

            if (!perCategoria[categoria][pezzo]) {
                perCategoria[categoria][pezzo] = 0;
            }

            perCategoria[categoria][pezzo] += componenti[pezzo];
        }
    }


    /* SELECT NORMALI */
    document.querySelectorAll("select[data-cat]").forEach(select => {

        let val = select.value;

        if (dati[val]) {
            aggiungi(dati[val], select.dataset.cat);
        }
    });


    /* CAMPATA INIZIALE */
    let iniziale = document.getElementById("campata_iniziale").value;

    if (dati[iniziale]) {
        aggiungi(dati[iniziale], "Campate");
    }


    /* CAMPATE INTERMEDIE */
    document.querySelectorAll("#listaCampate select").forEach(select => {

        let val = select.value;

        if (dati[val]) {
            aggiungi(dati[val], "Campate");
        }
    });


    /* CAMPATA FINALE */
    let finale = document.getElementById("campata_finale").value;

    if (dati[finale]) {
        aggiungi(dati[finale], "Campate");
    }


    /* STAMPA PER CATEGORIA */
    let divCat = document.getElementById("perCategoria");
    divCat.innerHTML = "";

    for (let cat in perCategoria) {

        let blocco = document.createElement("div");
        blocco.className = "categoria";

        let titolo = document.createElement("strong");
        titolo.textContent = cat;

        blocco.appendChild(titolo);

        let ul = document.createElement("ul");

        for (let pezzo in perCategoria[cat]) {

            let li = document.createElement("li");

            li.innerHTML = `
                <span class="qty">${perCategoria[cat][pezzo]}</span>
                <span class="sep">x</span>
                <span class="name">${pezzo}</span>
            `;

            ul.appendChild(li);
        }

        blocco.appendChild(ul);
        divCat.appendChild(blocco);
    }


    /* STAMPA TOTALE */
    let ulTot = document.getElementById("totale");
    ulTot.innerHTML = "";

    for (let pezzo in totale) {

        let li = document.createElement("li");

        li.innerHTML = `
            <span class="qty">${totale[pezzo]}</span>
            <span class="sep">x</span>
            <span class="name">${pezzo}</span>
        `;

        ulTot.appendChild(li);
    }
}


